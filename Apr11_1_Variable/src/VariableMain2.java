public class VariableMain2 {
	public static void main(String[] args) {
		// 식당 이름이 김밥천국
		// 출력
		// 테이블 수가 10개
		// 출력
		// 별점 4.5
		// 출력
		// 예약가능
		//출력
		String name = "김밥천국";
		System.out.println("식당");
		System.out.println(name);
		int tableCount = 10;
		System.out.println("테이블 수");
		System.out.println(tableCount);
		double star = 4.5;
		System.out.println("별점");
		System.out.println(star);
		boolean reservation = true;
		System.out.println(reservation);
	}
}